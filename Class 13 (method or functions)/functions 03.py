############################################## Lambda ####################################


sum = lambda a, b : a + b

print(sum(10, 20))

print((lambda a, b : a + b)(10, 20))


############################################## Map #######################################


my_list = [2, 3, 4, 5, 6, 7]


def square(x):

    return x * x

# Body


squared_values = map(square, my_list)

print(squared_values)

print(list(squared_values))

############################################## Filter ######################################


my_list = [2, 3, 4, 5, 6, 7]


def is_even(x):

    if x % 2 == 0:
        return True
    else:
        return False

# Body


even_numbers = filter(is_even, my_list)

print(even_numbers)

print(list(even_numbers))


